package com.wcaokaze.probosqis.app.core

import androidx.compose.animation.core.Animatable
import androidx.compose.animation.core.LinearEasing
import androidx.compose.animation.core.Spring
import androidx.compose.animation.core.spring
import androidx.compose.animation.core.tween
import androidx.compose.ui.unit.Dp
import androidx.compose.ui.unit.dp

internal suspend fun Animatable<Dp, *>.animateErrorNotifier() {
   animateTo(-12.dp, animationSpec = tween(32, easing = LinearEasing))
   animateTo(  0.dp, animationSpec = spring(Spring.DampingRatioHighBouncy))
}
